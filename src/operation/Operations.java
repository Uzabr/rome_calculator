package operation;

public class Operations {

        public int devide(int a, int b){
            int result = a / b;
            return result;
        }
        public int plus(int a, int b){
            int result = a + b;
            return result;
        }

        public int minus(int a, int b){
            int result = a - b;
            return result;
        }
        public int multiply(int a, int b){
            int result = a * b;
            return result;
        }
    }

